namespace EjemploPOO
{
    internal class OperacionesBasicas
    {
        public int Sumar(int primerNumero, int segundoNumero)
        {
            return primerNumero + segundoNumero;
        }

        public int Resta(int primerNumero, int segundoNumero)
        {
            return primerNumero - segundoNumero;
        }

        public int Multiplicacion(int primerNumero, int segundoNumero)
        {
            return primerNumero * segundoNumero;
        }

        public int Dividir(int primerNumero, int segundoNumero)
        {
            return primerNumero / segundoNumero; 
        }
    }
}
